    console.log(locationData);
    var marker = new google.maps.Marker({
      map: $scope.crapMap,
      position: new google.maps.LatLng(locationData.latitude, locationData.longitude)
    });
    $scope.myMarkers.push(marker);
    $scope.placeMarkerMap.push([
      locationData.id,
      marker
    ]);
